// @lc code=start
class Solution {
    public int numJewelsInStones(String jewels, String stones) {
        // 哈希
        return extracted(jewels, stones);

        // 暴力
        // return extracted2(jewels, stones);

    }

    /**
     * Your runtime beats 100 % of java submissions
     * Your memory usage beats 9.05 % of java submissions (40 MB)
     * 
     * @param jewels
     * @param stones
     * @return
     */
    private int extracted2(String jewels, String stones) {
        int jewelsCount = 0;
        int jewelsLength = jewels.length(), stonesLength = stones.length();
        for (int i = 0; i < stonesLength; i++) {
            char stone = stones.charAt(i);
            for (int j = 0; j < jewelsLength; j++) {
                char jewel = jewels.charAt(j);
                if (stone == jewel) {
                    jewelsCount++;
                    break;
                }
            }
        }
        return jewelsCount;
    }

    private int extracted(String jewels, String stones) {
        int jewelsCount = 0;
        Set<Character> jewelsSet = new HashSet<Character>();
        int jewelsLength = jewels.length(), stonesLength = stones.length();
        for (int i = 0; i < jewelsLength; i++) {
            char jewel = jewels.charAt(i);
            jewelsSet.add(jewel);
        }
        for (int i = 0; i < stonesLength; i++) {
            char stone = stones.charAt(i);
            if (jewelsSet.contains(stone)) {
                jewelsCount++;
            }
        }
        return jewelsCount;
    }
}
