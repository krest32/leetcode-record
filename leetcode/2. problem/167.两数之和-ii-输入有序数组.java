// @lc code=start
class Solution {
    public int[] twoSum(int[] numbers, int target) {
        // 双指针，定义左侧、右侧指针
        int i = 0;
        int j = numbers.length - 1;
        while (i < j) {
            int sum = numbers[i] + numbers[j];
            if (sum == target) {
                return new int[] { i + 1, j + 1 };
            } else if (sum > target) {
                j--;
            } else {
                i++;
            }
        }
        return new int[0];
    }
}
